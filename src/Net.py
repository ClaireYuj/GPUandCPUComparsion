class LeNet(nn.Module):

    def __init__(self, input_channel: int = 3 ,H: int = 28, W: int = 28):
        super(LeNet, self).__init__()
        # 3*3 conv
        self.net_name = "Lenet"
        self.conv1 = nn.Sequential(
            #  after convolution，C(Channels) change，but W(width) and H(Height) can change or not，depending on padding
            nn.Conv2d(input_channel, 16, 5, 1, 2),  #input_channel--1, out_channel--32, kernel_size--3 stride--1 padding特征图填充--2
            nn.ReLU(),
            nn.MaxPool2d(2, 2) # after pool--C(C.hannels) remains，W(width) and H(Height) change。
        )

        self.conv2 = nn.Sequential(
            nn.Conv2d(16, 32, 5),
            nn.ReLU(),
            nn.MaxPool2d(2, 2)  # max pool -- 2*2
        )

        self.fc1 = nn.Sequential(
            nn.Linear(32 * ((H//2-4)//2) * ((W//2-4)//2), 120),
            nn.BatchNorm1d(120),
            nn.ReLU()
        )

        self.fc2 = nn.Sequential(
            nn.Linear(120, 84),
            nn.BatchNorm1d(84),  # fasten 加快收敛速度的方法（注：批标准化一般放在全连接层后面，激活函数层的前面）
            nn.ReLU()
        )

        self.fc3 = nn.Linear(84, 10)


    def forward(self, x):
        self.H = x.shape[2]
        self.W = x.shape[3]
        x = self.conv1(x)
        x = self.conv2(x)
        x = x.view(x.size()[0], -1)
        # print("x:",x.shape)
        x = self.fc1(x)
        x = self.fc2(x)
        x = self.fc3(x)
        return x



